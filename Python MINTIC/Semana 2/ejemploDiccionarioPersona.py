''' Escribir un programa que pregunte al usuario su nombre, edad, direccion y telefono y lo guarde en el diccionario.
Después debe mostrar por pantalla el mensaje <nombre> tiene <edad> años, vive en <direccion> y su número de telefono es <telefono> '''

nombre = input("Por favor digite su nombre: ")
edad = input("Por favor digite su edad: ")
direccion = input("Por favor digite su dirección: ")
telefono = input("Por favor digite su teléfono: ")

persona = {"nombre":nombre ,"edad":edad , "direccion":direccion , "telefono":telefono}
print (persona["nombre"], " tiene " , persona["edad"] , " años y  vive en ", persona["direccion"], " y su número de telefono es ", persona["telefono"]) #Imprimir 1
print(f"{nombre} tiene {edad} años, vive en {direccion} y su número de telefono es {telefono}")                                                         #Imprimir 2 
